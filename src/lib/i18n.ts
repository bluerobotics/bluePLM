// Translation dictionary type - supports up to 3 levels of nesting
type TranslationValue = string | Record<string, string | Record<string, string>>
type TranslationDict = Record<string, TranslationValue>

// Flatten nested translation keys (e.g., { settings: { title: "Settings" } } -> { "settings.title": "Settings" })
function flattenTranslations(obj: TranslationDict, prefix = ''): Record<string, string> {
  const result: Record<string, string> = {}
  
  for (const [key, value] of Object.entries(obj)) {
    const fullKey = prefix ? `${prefix}.${key}` : key
    if (typeof value === 'string') {
      result[fullKey] = value
    } else {
      Object.assign(result, flattenTranslations(value as TranslationDict, fullKey))
    }
  }
  
  return result
}

